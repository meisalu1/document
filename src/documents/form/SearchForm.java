package documents.form;

import java.util.Map;

public class SearchForm {
	
	private String id;
	private String name;
	private String desc;
	private String last_modifier;
	private String cat_name;
	private String subject;
	private String attribute;
	private int currentStatusID;
	private int currentTypeID;
	private Map<String, String> attributes = null;

	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getDesc() {
		return desc;
	}
	
	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	public String getLast_modifier() {
		return last_modifier;
	}
	
	public void setLast_modifier(String last_modifier) {
		this.last_modifier = last_modifier;
	}
	
	public String getCat_name() {
		return cat_name;
	}
	
	public void setCat_name(String cat_name) {
		this.cat_name = cat_name;
	}
	
	public String getSubject() {
		return subject;
	}
	
	public void setSubject(String subject) {
		this.subject = subject;
	}
	
	public String getAttribute() {
		return attribute;
	}

	public void setAttribute(String attribute) {
		this.attribute = attribute;
	}
	
	public int getCurrentTypeID() {
		return currentTypeID;
	}

	public void setCurrentTypeID(int currentTypeID) {
		this.currentTypeID = currentTypeID;
	}
	
	public int getCurrentStatusID() {
		return currentStatusID;
	}
	
	public void setCurrentStatusID(int currentStatusID) {
		this.currentStatusID = currentStatusID;
	}

	public Map<String, String> getAttributes() {
		return attributes;
	}

	public void setAttributes(Map<String, String> attributes) {
		this.attributes = attributes;
	}
	
	
}
