package documents.model.data;

public class DocumentType {
	
	private int id;
	private String name;
	private DocumentType[] types = null;
	
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public DocumentType[] getTypes() {
		return types;
	}
	
	public void setTypes(DocumentType[] types) {
		this.types = types;
	}

}
